        </el-header>
         <el-container>
           <!-- 左边菜单 -->
           <el-aside width="200px">
                <el-menu :class="'menu'"
                          :default-active="editableTabsValue"
                          class="el-menu-vertical-demo"
                          :collapse="isCollapse"
                          background-color="#263238"
                          text-color="#fff"
                          active-text-color="#ffd04b"
                          @select="addMenuItem">
                <el-menu-item index="1">
                   <template slot="title">
                    <i class="el-icon-s-home"></i>
                    <span slot="title">主页</span>
                  </template>
                </el-menu-item>
                <el-submenu index="2">
