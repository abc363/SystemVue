                   </template>
                </el-menu-item>
              </el-menu>
          </el-aside>
          <el-container>
            <el-main>
            <el-tabs v-model="editableTabsValue" type="card" closable class="body-wrap" @tab-remove="removeTab">
              <el-tab-pane
                :key="item.name"
                v-for="item in editableTabs"
                :label="item.title"
                :name="item.name">
                  <component :is="item.component"></component>
              </el-tab-pane>
            </el-tabs>
            </el-main>
            <!-- 页脚 -->
            <el-footer class="main-footer" height="35px">© WindIIOTInventory.com - 后台管理系统</el-footer>
          </el-container>
        </el-container>
        
       
        </el-container>
    </div>
</template>

<script>
import Product from './Product';
import Home from './home';
import Sale from './sale';
import News from './news';
import Personal from './personal';
export default {
  // 在主页面上导入组件
    components: {
      Product,
      Home,
      Sale,
      News,
      Personal,
    },
    data() {
      // 菜单对象
       const menuMap=[
         {
            value: '0',
            label:'个人中心',
            component:'Personal',
          },
          {
            value: '1',
            label:'主页',
            component:'Home',
          },
          {
            value: '2-1',
            label:'产品管理',
            component:'Product',
          },
          {
            value: '2-2',
            label:'销售情况',
            component:'Sale',
          },
          {
            value: '3',
            label:'新闻管理',
            component:'News',
          },
        ];
        return {
            isCollapse: false,
            tabWidth: 200,
            test1: 1,
            editableTabsValue:'2-1',
            editableTabs: [],
            intelval: null,
            changeComponent:'Product',
            menuMap,
            tabIndex: 1,
        };
    },
    // 一打开就是产品管理菜单
    mounted(){
      this.addMenuItem('2-1');
    },
    methods: {
      // 添加菜单tab页
        addMenuItem(index){
         this.menuMap.forEach(item=>{
           if(item.value === index){
              const bol = this.editableTabs.every(tab=>{
                return tab.name !== index;
              });
              bol && this.editableTabs.push({
                title: item.label,
                name: index,
                component:item.component,
              });
              this.changeComponent = item.component;
              this.editableTabsValue = index;
           }
         })
        },
        // 退出登录
        outIndex(){
           this.$confirm('请问您是否要退出登录吗？', '提示', {
            confirmButtonText: '确定',
            cancelButtonText: '取消',
            type: 'warning'
          }).then(() => {
            this.post(`/users/logout`).then(res=>{
              location.href="./index.html";
              // 清除session
              window.sessionStorage.clear();
            }).catch(e=>{
              this.$error('退出失败，请重新尝试');
            })
          })
        },
        // 删掉菜单tab页
        removeTab(targetName) {
          let tabs = this.editableTabs;
          let activeName = this.editableTabsValue;
          if (activeName === targetName) {
            tabs.forEach((tab, index) => {
              if (tab.name === targetName) {
                let nextTab = tabs[index + 1] || tabs[index - 1];
                if (nextTab) {
                  activeName = nextTab.name;
                }
              }
            });
          }
          this.editableTabsValue = activeName;
          this.editableTabs = tabs.filter(tab => tab.name !== targetName);
      }
    }
}
</script>
