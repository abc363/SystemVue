                  </template>
                  <el-menu-item-group>
                    <el-menu-item index="2-1">产品管理</el-menu-item>
                    <el-menu-item index="2-2">销售情况</el-menu-item>
                  </el-menu-item-group>
                </el-submenu>
                <el-menu-item index="3">
                   <template slot="title">
                    <i class="el-icon-money" ></i>
